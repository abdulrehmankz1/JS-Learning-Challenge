// Functions in JavaScript:
// Definition: A function is a block of code designed to perform a specific task.
// Functions can take inputs (parameters), process them, and return an output (or result).
// Functions help in making code reusable and organized.

// In JavaScript, there are primarily three types of functions:

// 1. Function Declaration:
// This is the traditional way of defining a function with a specific name.

function greet(name) {
    console.log("Hello, " + name + "!");
}
greet("Abdul");

// 2. Function Expression:
// A function can be assigned to a variable, and this is called a function expression.

const greet = function (name) {
    console.log("Hello, " + name + "!");
};
greet("Rehman");
