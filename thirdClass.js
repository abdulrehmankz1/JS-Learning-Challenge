// Control Flow Statements:
// Definition: Control flow statements direct the order in which code is executed,
// allowing decisions to be made and different actions to occur based on conditions.

// Example 1: Basic if Statement

let number = 10;

if (number > 5) {
    console.log("The number is greater than 5.");
}

// Example 2: if-else Statement

let age = 20;

if (age >= 18) {
    console.log("You are eligible to vote.");
} else {
    console.log("You are not eligible to vote.");
}
